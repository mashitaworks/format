import fs from 'fs';
import path from 'path';

const envFile = process.env.NODE_ENV === 'production' ? '.env.production' : '.env';
const envPath = path.resolve(process.cwd(), envFile);
const outPath = path.resolve(process.cwd(), 'src/styles/imports/_env.scss');

const parseEnv = (text) => {
  return text
    .split(/\r?\n/)
    .filter((line) => line && !line.startsWith('#'))
    .reduce((acc, line) => {
      const [key, ...rest] = line.split('=');
      if (!key) return acc;
      acc[key.trim()] = rest.join('=').trim().replace(/^"|"$/g, '').replace(/^'|'$/g, '');
      return acc;
    }, {});
};

const env = fs.existsSync(envPath) ? parseEnv(fs.readFileSync(envPath, 'utf8')) : {};
const contentBaseUrl = env.SASS_CONTENT_BASE_URL || '/assets/';
const scss = `// This file is generated by scripts/generate-scss-env.mjs\n` +
`$content-base-url: "${contentBaseUrl}" !default;\n`;

fs.writeFileSync(outPath, scss, 'utf8');
console.log(`Generated ${path.relative(process.cwd(), outPath)} for NODE_ENV=${process.env.NODE_ENV || 'development'}`);
